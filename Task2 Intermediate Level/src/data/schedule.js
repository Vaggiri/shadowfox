// Helper function to get next match
export const getNextMatch = () => {
    const today = new Date();
    return schedule2026.find(match => new Date(match.date) > today) || schedule2026[0];
};

// Helper function to get home matches
export const getHomeMatches = () => {
    return schedule2026.filter(match => match.type === 'home');
};

// Helper function to get away matches
export const getAwayMatches = () => {
    return schedule2026.filter(match => match.type === 'away');
};
